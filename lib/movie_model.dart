class Movie {
  final String title;
  final String date;
  final double rating;
  final String imageUrl;
  final String genre;
  final String duration;
  final String synopsis;

  const Movie({
    required this.title,
    required this.date,
    required this.rating,
    required this.imageUrl,
    required this.genre,
    required this.duration,
    required this.synopsis,
  });
}
